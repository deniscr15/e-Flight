    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $post = $request->all();
        $post['date'] = Carbon::parse($post['date'])->format('Y-m-d');

        $flight = Flight::create($post);

        return redirect(route('flights.index'))->with('success','Created successfully!');
    }

